#include<iostream>
using namespace std;
int main()
{
    // This program for even and odd operator
    int number;
    cout<<"Please enter the whole number:- ";
    cin>>number;
    if(number%2==0)
    {
        cout<<"This number is even number "<<number;
    }
    else
    {
        cout<<"This number is a odd number "<<number<<endl;
    }
    cout<<"Thanks, Bye";
}
